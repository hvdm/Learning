# New-Item            -- Creates a new item
# Get-Item            -- Returns details about the specified item, doesn't matter if you are running it on a folder or registry item
# Set-Item            -- Sets the value of an item
# Rename-item         -- Renames the specified item
# Move-Item           -- Move an item from one location to another
# Clear-Item          -- Clears the value of an item
# Copy-Item           -- Copies an item from one location to another
# Remove-Item         -- Removes the specified item


# Get-ChildItem       -- Returns child objects of the current item

# New-ItemProperty    -- Creates a new property of an item
# Get-ItemProperty    -- Retrieves the properties of a specified item
# Set-ItemProperty    -- Sets the value of an item's property
# Rename-ItemProperty -- renames the property of an item
# Move-ItemProperty   -- Move an item property from one item to another
# Clear-ItemProperty  -- Clears the propert value of an item
# Copy-ItemProperty   -- Copies the property + Value from one item to another



